/**
 * Reactive translation function. Returns the translated string for the current locale,
 * or falls back to English, or returns the key itself.
 */
export function t(key: string): string {
  const loc = get(locale);
  return translations[loc]?.[key] ?? translations['en']?.[key] ?? key;
}

/**
 * Derived store that re-evaluates whenever locale changes, providing a reactive t function.
 */
export const tStore = derived(locale, ($locale) => {
  return (key: string): string => {
    return translations[$locale]?.[key] ?? translations['en']?.[key] ?? key;
  };
});
